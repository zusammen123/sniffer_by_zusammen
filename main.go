package main

import (
	"os"

	"fyne.io/fyne/v2"
	"fyne.io/fyne/v2/app"
	"fyne.io/fyne/v2/widget"
)

func main() {
	// 设置中文字体，不然中文乱码
	os.Setenv("FYNE_FONT", "msyhl.ttc")
	a := app.New()
	//创建一个窗口
	w := a.NewWindow("gui编程")
	//设置窗口内显示内容，需要中文字体
	w.SetContent(widget.NewLabel("go GUI 编程"))
	// 设置窗口大小
	w.Resize(fyne.NewSize(400, 400))
	// 窗口居中
	w.CenterOnScreen()
	//展示窗口 并运行程序
	w.ShowAndRun()
}
